        async function detectLanguage() {
            try {
                // Try to get user's country from IP
                const response = await fetch('https://ipapi.co/json/');
                const data = await response.json();
                const country = data.country_code;

                // Map countries to languages
                if (['LB', 'SY', 'JO', 'EG', 'SA', 'AE', 'IQ', 'YE', 'OM', 'KW', 'BH', 'QA', 'PS', 'DZ', 'MA', 'TN', 'LY', 'SD', 'MR'].includes(country)) {
                    currentLang = 'ar';
                } else if (['VE', 'ES', 'MX', 'AR', 'CO', 'PE', 'CL', 'EC', 'GT', 'CU', 'BO', 'DO', 'HN', 'PY', 'SV', 'NI', 'CR', 'PA', 'UY', 'GQ'].includes(country)) {
                    currentLang = 'es';
                } else {
                    currentLang = 'en';
                }
            } catch (error) {
                console.log('Could not detect location, using English');
                currentLang = 'en';
            }

            applyTranslations();
        }

        function applyTranslations() {
            const t = translations[currentLang];
            
            // Update HTML lang and dir attributes
            document.documentElement.lang = currentLang;
            document.documentElement.dir = currentLang === 'ar' ? 'rtl' : 'ltr';
            document.body.dir = currentLang === 'ar' ? 'rtl' : 'ltr';
            
            // Update title
            document.title = t.title;
            
            // Update all text content
            document.querySelectorAll('[data-i18n]').forEach(element => {
                const key = element.getAttribute('data-i18n');
                if (t[key]) {
                    if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                        element.placeholder = t[key];
                    } else {
                        element.textContent = t[key];
                    }
                }
            });

            // Update language display
            updateLanguageDisplay();
        }

